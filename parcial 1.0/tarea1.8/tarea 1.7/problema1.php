<h2>Calculos condicionales</h2>

<h3>Descripción</h3>
Eres muy bueno evaluando fórmulas y realizando cálculos directos, pero te cuesta trabajo decidir en qué situaciones se deben hacer qué operaciones. Un compañero te sugirió un ejercicio de cálculo mental que consiste en, dado un número inicial 
N , realizar las siguientes operaciones en el orden indicado y con aritmética entera: <br>
Si el valor actual de 
  N es par, divídelo entre 2. En caso contrario, súmale 1.
Si el valor actual de N
 tiene tres o más cifras, divídelo entre 100. Pero si tiene exactamente dos cifras, divídelo entre 10.
Si el valor actual de N
 es un múltiplo de tres, réstale uno.

<h3>Entrada</h3>
Un entero N. Puedes suponer que 1<= N <= 1000.

<h3>Salida</h3>
Dos enteros que correspondan con el valor final de N
 y el número de veces que N fue modificado respectivamente.

