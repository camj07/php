<h2 class="letra">Escalera de asteriscos</h2>

<h3>Descripción</h3>
Inés quiere llegar a su casa, pero accidentalmente, los arquitectos construyeron la puerta demasiado alta. Tu misión es ayudar a Inés a subir a su casa construyendo una escalera de longitud y altura 
  N hecha de asteriscos.

<h3>Entrada</h3>
Se te darán como valor de entrada un numero entero N 
 el cuál representa la longitud y altura de la escalera.


<h3>Salida</h3>
Deberás imprimir como salida una escalera de dimensiones N  de largo y N de alto

